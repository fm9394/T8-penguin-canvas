function input(
  key: string,
  label: string,
  kind: FalToolboxInputMapping['kind'],
  options: Partial<FalToolboxInputMapping> = {},
): FalToolboxInputMapping {
  return { key, label, kind, required: true, upload: kind !== 'text', mediaMode: kind === 'image' ? 'base64' : 'url', ...options };
}

function promptInput(key = 'prompt', label = 'Prompt', required = true): FalToolboxInputMapping {
  return input(key, label, 'text', { required, upload: false });
}

function selectParam(key: string, label: string, defaultValue: string | number, options: Array<string | number>): FalToolboxUserParam {
  return { key, label, kind: 'select', defaultValue, options };
}

function numberParam(key: string, label: string, defaultValue: number, min: number, max: number, step = 1): FalToolboxUserParam {
  return { key, label, kind: 'number', defaultValue, min, max, step };
}

function boolParam(key: string, label: string, defaultValue: boolean): FalToolboxUserParam {
  return { key, label, kind: 'boolean', defaultValue };
}

function textParam(key: string, label: string, defaultValue = '', textarea = false): FalToolboxUserParam {
  return { key, label, kind: textarea ? 'textarea' : 'text', defaultValue };
}

function jsonParam(key: string, label: string, payloadKey: string): FalToolboxUserParam {
  return { ...textParam(key, label), payloadKey, parseJson: true, omitValues: [''] };
}

function tool(config: FalToolboxTool): FalToolboxTool {
  return config;
}

